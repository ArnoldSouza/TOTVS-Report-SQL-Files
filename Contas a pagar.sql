SELECT

	SE2010.E2_NATUREZ AS 'NATUREZA',
	RTrim(SED010.ED_DESCRIC) AS 'DESCRIÇÃO NATUREZA',
	
	(
	SELECT TOP 1 SD1010.D1_TES
	FROM PROTHEUS.dbo.SD1010 SD1010
	WHERE SD1010.D1_DOC=SE2010.E2_NUM AND SD1010.D1_FORNECE=SE2010.E2_FORNECE AND SD1010.D1_LOJA=SE2010.E2_LOJA AND SD1010.D_E_L_E_T_<>'*'
	) AS 'TES',
	
	(
	SELECT TOP 1 SF4010.F4_TEXTO AS 'DESCRIÇÃO TES'
	FROM PROTHEUS.dbo.SD1010 SD1010
		LEFT JOIN PROTHEUS.dbo.SF4010 SF4010 ON SF4010.F4_CODIGO=SD1010.D1_TES
	WHERE SD1010.D1_DOC=SE2010.E2_NUM AND SD1010.D1_FORNECE=SE2010.E2_FORNECE AND SD1010.D1_LOJA=SE2010.E2_LOJA AND SD1010.D_E_L_E_T_<>'*'
	) AS 'DESCRIÇÃO TES',
	
	(
	SELECT TOP 1 SF4010.F4_ESTOQUE  AS 'ATU. ESTOQUE'
	FROM PROTHEUS.dbo.SD1010 SD1010
		LEFT JOIN PROTHEUS.dbo.SF4010 SF4010 ON SF4010.F4_CODIGO=SD1010.D1_TES
	WHERE SD1010.D1_DOC=SE2010.E2_NUM AND SD1010.D1_FORNECE=SE2010.E2_FORNECE AND SD1010.D1_LOJA=SE2010.E2_LOJA AND SD1010.D_E_L_E_T_<>'*'
	) AS 'ATU. ESTOQUE',
	
	(
	SELECT TOP 1 SF4010.F4_ATUATF AS 'ATU. ATIVO'
	FROM PROTHEUS.dbo.SD1010 SD1010
		LEFT JOIN PROTHEUS.dbo.SF4010 SF4010 ON SF4010.F4_CODIGO=SD1010.D1_TES
	WHERE SD1010.D1_DOC=SE2010.E2_NUM AND SD1010.D1_FORNECE=SE2010.E2_FORNECE AND SD1010.D1_LOJA=SE2010.E2_LOJA AND SD1010.D_E_L_E_T_<>'*'
	) AS 'ATU. ATIVO',
	
	(
	SELECT TOP 1 SD1010.D1_PEDIDO  AS 'NUM PEDIDO'
	FROM PROTHEUS.dbo.SD1010 SD1010
		LEFT JOIN PROTHEUS.dbo.SF4010 SF4010 ON SF4010.F4_CODIGO=SD1010.D1_TES
	WHERE SD1010.D1_DOC=SE2010.E2_NUM AND SD1010.D1_FORNECE=SE2010.E2_FORNECE AND SD1010.D1_LOJA=SE2010.E2_LOJA AND SD1010.D_E_L_E_T_<>'*'
	) AS 'NUM PEDIDO',
	
	SE2010.E2_PREFIXO AS 'PREFIXO TITULO',
	SE2010.E2_FATPREF AS 'PREFIXO FATURA',
	SE2010.E2_NUM AS 'NUM DOCUMENTO',
	SE2010.E2_VALLIQ AS 'VAL LIQ BAIXADO',
	SE2010.E2_VLCRUZ AS 'VALOR R$',
	SE2010.E2_VALOR AS 'VALOR DO TÍTULO',
	SE2010.E2_SALDO AS 'SALDO',
	SE2010.E2_PARCELA AS 'PARCELA',
	SE2010.E2_TIPO AS 'TIPO',
	SE2010.E2_FORNECE AS 'COD FORNECEDOR',
	SE2010.E2_NOMFOR AS 'NOME FORNECEDOR',
	SE2010.E2_LOJA AS 'LOJA',
	SE2010.E2_CCD AS 'C CUSTO DEB',
	RTRIM(CTT010.CTT_DESC01) AS 'DESCRIÇÃO CENTRO DE CUSTO', 
	Case RTrim(Coalesce(SE2010.E2_EMISSAO,'')) WHEN ''  THEN Null ELSE convert(datetime,SE2010.E2_EMISSAO, 112)END AS 'DT EMISSÃO',
	Case RTrim(Coalesce(SE2010.E2_VENCTO,'')) WHEN ''  THEN Null ELSE convert(datetime,SE2010.E2_VENCTO, 112)END AS 'DT VENCIMENTO',
	Case RTrim(Coalesce(SE2010.E2_VENCREA,'')) WHEN ''  THEN Null ELSE convert(datetime,SE2010.E2_VENCREA, 112)END AS 'DT VENCIMENTO REAL',
	Case RTrim(Coalesce(SE2010.E2_BAIXA,'')) WHEN ''  THEN Null ELSE convert(datetime,SE2010.E2_BAIXA, 112)END AS 'DT BAIXA',
	Case RTrim(Coalesce(SE2010.E2_EMIS1,'')) WHEN ''  THEN Null ELSE convert(datetime,SE2010.E2_EMIS1, 112)END AS 'DT CONTAB',
	RTrim(SE2010.E2_HIST) AS 'HISTÓRICO',
	RTrim(SE2010.E2_DETHIST) AS 'DETALHES HISTÓRICO',
	RTrim(SE2010.E2_INFCOMP) AS 'INFORMAÇÕES COMPLEMENTARES',
	SE2010.E2_NUMBOR AS 'NUM BORDERO',
	Case RTrim(Coalesce(SE2010.E2_DTBORDE,'')) WHEN ''  THEN Null ELSE convert(datetime,SE2010.E2_DTBORDE, 112)END AS 'DT BORDERO',
	SE2010.E2_FATURA AS 'NUM FATURA',
	SE2010.E2_RATEIO AS 'RATEIO',
	SE2010.E2_ORIGEM AS 'ORIGEM',
	SE2010.E2_TIPOFAT AS 'TIPO FATURA',
	SE2010.E2_FILORIG AS 'FILIAL DE ORIGEM',
	SE2010.E2_FATFOR AS 'FORNECEDOR DA FATURA',
	SE2010.E2_FATLOJ AS 'LOJA FOR FAT',
	SUBSTRING(SE2010.E2_BAIXA,5,2)+' - '+LEFT(SE2010.E2_BAIXA,4) AS 'ANO-MÊS'

FROM PROTHEUS.dbo.SE2010 SE2010
	LEFT JOIN PROTHEUS.dbo.CTT010 CTT010 ON SE2010.E2_CCD=CTT010.CTT_CUSTO
	LEFT JOIN PROTHEUS.dbo.SED010 SED010 ON SED010.ED_CODIGO=SE2010.E2_NATUREZ

WHERE
--SE2010.E2_CCD='         ' AND
SE2010.D_E_L_E_T_<>'*' AND
SE2010.E2_BAIXA>='20170101' AND
SE2010.E2_NOMFOR LIKE '%PERTENCER%'
