SELECT

	SF8010.F8_FILIAL AS 'FILIAL',
	SF8010.F8_NFDIFRE AS 'FRETE-NF',
	SF8010.F8_SEDIFRE AS 'FRETE-SERIE',
	Case RTrim(Coalesce(SF8010.F8_DTDIGIT,'')) WHEN ''  THEN Null ELSE convert(datetime,SF8010.F8_DTDIGIT, 112)END AS 'FRETE-DT DIGITAÇÃO',
	SF8010.F8_TRANSP AS 'FRETE-TRANSPORTADORA',
	SF8010.F8_LOJTRAN AS 'FRETE-LOJA',
	SF8010.F8_NFORIG AS 'ORIGEM-NF',
	SF8010.F8_SERORIG AS 'ORIGEM-SERIE',
	SF8010.F8_FORNECE AS 'ORIGEM-FORNECEDOR',
	SF8010.F8_LOJA AS 'ORIGEM-LOJA',
	SF8010.F8_TIPO AS 'ORIGEM-TIPO'

FROM PROTHEUS.dbo.SF8010 SF8010

WHERE 
	D_E_L_E_T_<>'*' AND
	SF8010.F8_DTDIGIT >='20160101'
	