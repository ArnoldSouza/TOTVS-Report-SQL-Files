SELECT TOP 100 
	CONVERT(VARCHAR(MAX), CONVERT(VARBINARY(MAX), ZE3_MEMO)) AS 'MOTIVO',
	*
FROM ZE3010
WHERE
	ZE3_FILIAL = '07' AND
	D_E_L_E_T_ <> '*' AND
	(ZE3_USRINC = '001501') AND
	ZE3_DATA >= '20180101'
	--AND CONVERT(VARCHAR(MAX), CONVERT(VARBINARY(MAX), ZE3_MEMO)) LIKE '%ALICATE%'
ORDER BY 
	ZE3_DATA DESC
	--ZE3_CODFUN = '816626'