SELECT --TOP 1000
	SRD010.RD_FILIAL AS 'FILIAL',
	SRD010.RD_MAT AS 'NUM MAT',
	RTRIM(SRA010.RA_NOME) AS 'NOME DO FUNCIONARIO',
	RTRIM(SRJ010.RJ_DESC) AS 'DESC FUNÇÃO',
	SRD010.RD_PD AS 'COD VERBA',
	RTRIM(SRV010.RV_DESCDET) AS 'DESC VERBA',
	CASE
		WHEN SRD010.RD_TIPO1='V' THEN 'VALOR'
		WHEN SRD010.RD_TIPO1='H' THEN 'HORAS'
		WHEN SRD010.RD_TIPO1='D' THEN 'DIAS'
		ELSE 'ERRO'
	END AS 'TP INFO',
	SRD010.RD_HORAS AS 'HORAS ACUM',
	SRD010.RD_VALOR AS 'VALOR ACUM',
	SRD010.RD_DATARQ AS 'MES/ANO',
	Case RTrim(Coalesce(SRD010.RD_DATPGT,'')) WHEN ''  THEN Null ELSE convert(DATETIME, SRD010.RD_DATPGT, 112)END AS 'DT PAGAMENTO',
	SRD010.RD_SEMANA AS 'SEMANA DA VERBA',

	--IDENTIFICA LOCAL
	REPLACE(RTRIM(CTT010.CTT_XREGIO),'_',' ') AS 'REGIONAL',
	IIF(
		REPLACE(REPLACE(RTRIM(CTT_XPOLO),'_',' '),'POLO ', '')=REPLACE(RTRIM(CTT010.CTT_XREGIO),'_',' '),
		REPLACE(REPLACE(RTRIM(CTT_XPOLO),'_',' '),'POLO ', ''),
		REPLACE(REPLACE(REPLACE(RTRIM(CTT_XPOLO),'_',' '),'POLO ', ''), REPLACE(RTRIM(CTT010.CTT_XREGIO),'_',' ')+' ', '')
	) AS 'POLO',
	SRD010.RD_CC AS 'CENTRO CUSTO',
	RTRIM(CTT010.CTT_DESC01) AS 'DESCRIÇÃO CENTRO DE CUSTO',

	CASE
		WHEN SRD010.RD_TIPO2='I' THEN 'INFORMADA'
		WHEN SRD010.RD_TIPO2='G' THEN 'GERADA'
		WHEN SRD010.RD_TIPO2='E' THEN 'PONTO ELETRÔNICO'
		WHEN SRD010.RD_TIPO2='K' THEN 'FERIAS'
		WHEN SRD010.RD_TIPO2='P' THEN '1A PARCELA 13'
		WHEN SRD010.RD_TIPO2='S' THEN '2A PARCELA 13'
		WHEN SRD010.RD_TIPO2='C' THEN 'CALCULADO'
		WHEN SRD010.RD_TIPO2='A' THEN 'ADIANTAMENTO'
		WHEN SRD010.RD_TIPO2='R' THEN 'RESCISÃO'
		WHEN SRD010.RD_TIPO2='F' THEN 'FECHAMENTO'
		ELSE 'ERRO'
	END AS 'TP INFO',

	SRD010.RD_MES AS 'MES',
	CASE
		WHEN SRD010.RD_STATUS='A' THEN 'ACUMULADO'
		WHEN SRD010.RD_STATUS='M' THEN 'MODIFICADO'
		WHEN SRD010.RD_STATUS='I' THEN 'INCLUIDO'
		ELSE 'ERRO'
	END AS 'TP STATUS',
	SRD010.RD_INSS AS 'INSS',
	SRD010.RD_IR AS 'IRRF',
	SRD010.RD_FGTS AS 'FGTS',
	--SRD010.RD_PROCES AS 'CODIGO PROCESSO', --O QUE É COD PROCESSO?
	--SRD010.RD_PERIODO AS 'CODIGO PERIODO',  --INFO REPETIDA
	--SRD010.RD_ROTEIR AS 'ROTEIRO',  INFO DESNECESSÁRIA
	--SRD010.RD_DTREF AS 'DT REFERENCIA',
	--SRD010.RD_NUMID AS 'NUMERO DE IDENTIFICACAO',
	SRD010.RD_DEPTO AS 'COD DPTO',
	RTRIM(SQB010.QB_DESCRIC) AS 'DESC DEPARTAMENTO',
	SRD010.RD_CODB1T AS 'SEQUENCIA LANCTO',
	SRD010.RD_VALORBA AS 'VALOR BASE'
FROM PROTHEUS.dbo.SRD010 SRD010
	LEFT JOIN PROTHEUS.dbo.SRA010 SRA010 ON SRD010.RD_MAT=SRA010.RA_MAT AND SRA010.D_E_L_E_T_<>'*' --CADASTRO FUNCIONÁRIOS
	LEFT JOIN PROTHEUS.dbo.SRV010 SRV010 ON SRD010.RD_PD=SRV010.RV_COD AND SRV010.D_E_L_E_T_<>'*' --CADASTRO DE VERBAS
	LEFT JOIN PROTHEUS.dbo.SQB010 SQB010 ON SRA010.RA_DEPTO=SQB010.QB_DEPTO AND SQB010.D_E_L_E_T_<>'*' --CADASTRO DEPARTAMENTOS
	LEFT JOIN PROTHEUS.dbo.CTT010 CTT010 ON SRA010.RA_CC=CTT010.CTT_CUSTO AND CTT010.D_E_L_E_T_<>'*' --CADASTRO CENTRO DE CUSTO
	LEFT JOIN PROTHEUS.dbo.SRJ010 SRJ010 ON SRA010.RA_CODFUNC=SRJ010.RJ_FUNCAO AND SRJ010.D_E_L_E_T_<>'*'  --CADASTRO DE FUNÇÕES
WHERE
	SRD010.D_E_L_E_T_<>'*' AND
	SRD010.RD_MAT = '016035' AND
	LEFT(SRD010.RD_DATARQ,6) IN ('202005') AND
	SRD010.RD_PD IN ('089','030','021','022','080','839','462','447','297','071','070','092','094','101','090','091','018','214','103','049','051','050','052','001','342','779') --AND
	--SRD010.RD_MAT IN ('011356','008455','008516','013731','014559','016871','017573','016784','014010','018289','010443','015958','016907','019437','018688','018465','018587','016760','010958','020068','018464','016942','016940','015728','019819','018833','018834','016978','016696','015681','018469','019130','018471','011200','020076','018773','018421','019364','018472','015155','018473','016075','018676','019764','017571','017600','018605','017393','018167','017642','013967','018836','017466','018135','018240','018743','019635','018468','018663','020025','016405','019691','019169','019315','019543','019414','019801','019413','019988','018692','019170','020106','020219','019544','020096','020220','018824','016639','016402','017727','017890','018612','018611','016699','018271','019309','018751','018290','019998','017246','017277','017189','019360','019310','015883','019450','019441','019429','020120','016401','008581','016035','010737','010838','015569','017932','017719','016939','013017','018385','018949','020245','020246')
ORDER BY
	SRD010.R_E_C_N_O_ DESC, SRD010.RD_PD ASC